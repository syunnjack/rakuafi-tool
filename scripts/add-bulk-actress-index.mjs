function unique(values) {
  return [...new Set(values.filter(Boolean))]
}

function slugifyName(name) {
  return encodeURIComponent(name)
}

function createIndexRecord(name) {
  return {
    name,
    aliases: aliasMap.get(name) || [],
    work: '女優名検索インデックス',
    code: `INDEX-${name}`,
    maker: 'FANZA / DUGA / APEX / MGS動画 / SOD / DTICASH API同期対象',
    tags: unique([
      name,
      ...(aliasMap.get(name) || []),
      '大量データ',
      '検索索引',
      '女優名',
      'MGS動画',
      'SOD',
      'DTICASH',
      'DTI CASH',
      'DTI',
      'API補完対象',
    ]),
    source: '初期検索インデックス',
    sourceUrl: `https://www.dmm.co.jp/search/=/searchstr=${slugifyName(name)}/`,
  }
}

function mergeRecords(records) {
  const merged = new Map()
  for (const record of records) {
    const key = record.name
    const existing = merged.get(key)
    if (!existing) {
      merged.set(key, record)
      continue
    }
    merged.set(key, {
      ...record,
      ...existing,
      aliases: unique([...(record.aliases || []), ...(existing.aliases || [])]),
      tags: unique([...(record.tags || []), ...(existing.tags || [])]),
      source: existing.source || record.source,
      sourceUrl: existing.sourceUrl || record.sourceUrl,
    })
  }
  return [...merged.values()]
}

const existing = JSON.parse(await readFile(DATA_PATH, 'utf8'))
const seedRecords = unique(seedNames).map(createIndexRecord)
const merged = mergeRecords([...seedRecords, ...existing])

await writeFile(DATA_PATH, `${JSON.stringify(merged, null, 2)}\n`)
console.log(`Added ${seedRecords.length} seed actress names. Total records: ${merged.length}`)
